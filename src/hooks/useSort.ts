import { useState, useMemo } from "react";

interface Product {
  id: string;
  imageUrl: string;
  name: string;
  size: string;
  category: string;
  subcategory: string | null;
  fullPath: string;
  price: number;
  shippedFrom: string;
  shippedTo: string[];
  displayName?: string; // âœ… dukung nama tampil
  allImages?: string[];
}

export const useSort = (products: Product[]) => {
  const [sortOption, setSortOption] = useState("default");

  // âœ… Logika penentuan produk Best Selling
  const isBestSelling = (p: Product) => {
    if (!p.displayName || !p.category) return false;

    const name = (p.displayName || p.name).toLowerCase().trim();
    const category = p.category.toLowerCase().trim();

    // âœ… 3D Category: hanya 12R & 10R (bukan "by AI")
    if (
      category.includes("3d") &&
      (/\b12r\b/.test(name) || /\b10r\b/.test(name)) &&
      !name.includes("by ai")
    ) {
      return true;
    }

    // âœ… 2D Category: hanya 8R
    if (category.includes("2d") && /\b8r\b/.test(name)) {
      return true;
    }

    // âœ… Acrylic Stand 2cm (atau 2 cm)
    if (name.includes("acrylic stand") && (name.includes("2cm") || name.includes("2 cm"))) {
      return true;
    }

    return false;
  };

  const sortedProducts = useMemo(() => {
    if (sortOption === "best-selling") {
      // ðŸ”¹ Filter hanya produk yang sesuai best selling
      return products.filter(isBestSelling);
    }

    // ðŸ”¹ Sorting harga
    switch (sortOption) {
      case "price-asc":
        return [...products].sort((a, b) => a.price - b.price);
      case "price-desc":
        return [...products].sort((a, b) => b.price - a.price);
      default:
        return products;
    }
  }, [products, sortOption]);

  return { sortedProducts, sortOption, setSortOption };
};